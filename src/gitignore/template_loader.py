def load_template(template_name: str, with_comments: bool = False) -> list:
    template_url = GITIGNORE_TEMPLATES[template_name]
    responce = requests.get(template_url)
    entries = responce.content.decode().split('\n')
    if not with_comments:
        entries = [e for e in entries if e.strip() and not e.strip().startswith('#')]
    
    return entries

    
